public class Rumus {
    int sisi;
    int panjang;
    int lebar;
    int hasil;
    
    void setLuasPersegi() {
        this.hasil = this.sisi * this.sisi;
    }
    
    void setLuasPersegiPanjang() {
        this.hasil = this.panjang * this.lebar;
    }
}
